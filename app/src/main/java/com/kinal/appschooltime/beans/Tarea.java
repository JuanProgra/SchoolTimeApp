public class Tarea {
    public String NombreTare;
    public String Nombre;
    public String Detalle;
    public String Fecha;
    public String Punteo;

    public String getNombreTare() {
        return NombreTare;
    }

    public void setNombreTare(String nombreTare) {
        NombreTare = nombreTare;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public String getDetalle() {
        return Detalle;
    }

    public void setDetalle(String detalle) {
        Detalle = detalle;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String fecha) {
        Fecha = fecha;
    }

    public String getPunteo() {
        return Punteo;
    }

    public void setPunteo(String punteo) {
        Punteo = punteo;
    }
}
